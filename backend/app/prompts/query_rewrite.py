"""查询改写：用于向量检索的提示词。"""


def build_query_rewrite_prompt(question: str, n: int) -> str:
    return f"""你是一个「查询改写器」。给定用户问题，生成 {n} 条更利于向量检索的查询。

要求：
- 每行一条查询，不要编号以外的多余说明
- 保留问题中的核心实体与术语；可补充常见同义词或英文缩写（若有）
- 多角度覆盖：至少包含「直接问法」「关键词/术语检索」「定义或背景类」中的不同侧重（视问题类型选 2～3 类）
- 若问题含多个子问题，拆成对应子查询
- 若涉及操作/流程，可含「步骤」「方法」「如何」等词
- 若涉及定义，可含「是什么」「定义」等词
- 只输出查询列表，每行一条

问题：{question}
"""
